        $resultPage->addBreadcrumb(__('Ktpl'), __('Ktpl'));
        $resultPage->addBreadcrumb(__('Manage item'), __('Manage item'));
        $resultPage->getConfig()->getTitle()->prepend(__('Manage item'));

        return $resultPage;
